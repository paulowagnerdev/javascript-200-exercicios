const boolOne = true;
const booTwo = false;

console.log(`OPERADOR E true e false: ${boolOne&&booTwo}`);
console.log(`OPERADOR OR true e false: ${boolOne||booTwo}`);
console.log(`OPERADOR NOT true e false: ${boolOne!=booTwo}`);